package CantoniLib;

/**
 * Library containing various methods used to 
 * work with arrays
 * 
 * @author Cantoni Alessandro
 */
public final class Arr {
      private Arr() {}
      
      /**
      * Array utility class containing common sorting
      * algorithms
      * 
      * @author Cantoni Alessandro
      */
      public static final class Sorts extends arr.Sorts {}
      
      /**
      * Array utility class with methods to print the array
      * in console
      * 
      * @author Cantoni Alessandro
      */
      public static final class Console extends arr.Console {}
      
      /**
      * Array utility class that allows peremutations of
      * the arrat
      * 
      * @author Cantoni Alessandro
      */
      public static final class Perm extends arr.Perm {}
      
      /**
      * Array utility class used to do operations
      * on the elements of the array or get informations
      * about them
      * 
      * @author Cantoni Alessandro
      */
      public static final class Operations extends arr.Operations {}
}
